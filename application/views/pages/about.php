					<p>
						Consonant-cluster (Samyuktakashar) is one of the most interesting aspects of the Devnagari 
						Script. Two or more consonants come together to form a Samyuktakshar. This combination of 
						letters creates a unique visual representation. These representations are extremely important in the study of Devnagari Script. To a calligrapher and a typographer, it is pleasure to see how 
						these letters come together to create an altogether new form.
